package ufps.mincit.datos.dto;

/**
 *
 * @author user
 */
public class LogroDTO {
    
    private int id;
    private String titulo;

    public LogroDTO() {
    }

    public LogroDTO(int id, String titulo) {
        this.id = id;
        this.titulo = titulo;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getTitulo() {
        return titulo;
    }

    public void setTitulo(String titulo) {
        this.titulo = titulo;
    }
    
    
}
